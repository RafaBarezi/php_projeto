Marcação da tag php vista no inicio do código (<?php) serve para ajudar na identificação.
Todo arquivo extensão PHP é lida pelo servidor, enviado para o módulo PHP irá verificar as marcações contidas neste arquivo.

A tarefa do PHP é gerar código HTML, portanto nosso PHP estará presente antes do nosso HTML, dentro dele, ou teremos uma mescla, com códigos de interpretação, programação e texto. POrtanto não é obrigatória a presença de PHP dentro de um arquivo PHP.

A marcação serve para mostrar que o código não pode ser enviado para o cliente, e que o o servidor deve interpretar tudo o que estiver marcado com esta simbologia.

Existe outra forma de marcação do php : <script language="php"></script> mas a mais comum e recomendada é a <?php.

É muito comum encontrar tags abertas (sem o ?>) pois caso seja inserido um código após o fechamento, tal não será executado pelo servidor. É recomendado omitir o fechamento.                                                                       
